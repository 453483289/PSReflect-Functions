$THREAD_ACCESS = psenum $Mod THREAD_ACCESS UInt32 @{
    THREAD_TERMINATE                 = 0x00000001
    THREAD_SUSPEND_RESUME            = 0x00000002
    THREAD_GET_CONTEXT               = 0x00000008
    THREAD_SET_CONTEXT               = 0x00000010
    THREAD_SET_INFORMATION           = 0x00000020
    THREAD_QUERY_INFORMATION         = 0x00000040
    THREAD_SET_THREAD_TOKEN          = 0x00000080
    THREAD_IMPERSONATE               = 0x00000100
    THREAD_DIRECT_IMPERSONATION      = 0x00000200
    THREAD_SET_LIMITED_INFORMATION   = 0x00000400
    THREAD_QUERY_LIMITED_INFORMATION = 0x00000800
    DELETE                           = 0x00010000
    READ_CONTROL                     = 0x00020000
    WRITE_DAC                        = 0x00040000
    WRITE_OWNER                      = 0x00080000
    SYNCHRONIZE                      = 0x00100000
    THREAD_ALL_ACCESS = $THREAD_ACCESS::DELETE -bor $THREAD_ACCESS::READ_CONTROL -bor $THREAD_ACCESS::SYNCHRONIZE -bor $THREAD_ACCESS::WRITE_DAC -bor $THREAD_ACCESS::WRITE_OWNER -bor $THREAD_ACCESS::THREAD_DIRECT_IMPERSONATION -bor $THREAD_ACCESS::THREAD_GET_CONTEXT -bor $THREAD_ACCESS::THREAD_IMPERSONATE -bor $THREAD_ACCESS::THREAD_QUERY_INFORMATION -bor $THREAD_ACCESS::THREAD_QUERY_LIMITED_INFORMATION -bor $THREAD_ACCESS::THREAD_SET_CONTEXT -bor $THREAD_ACCESS::THREAD_SET_LIMITED_INFORMATION -bor $THREAD_ACCESS::THREAD_SET_THREAD_TOKEN -bor $THREAD_ACCESS::THREAD_SUSPEND_RESUME -bor $THREAD_ACCESS::THREAD_TERMINATE
} -Bitfield